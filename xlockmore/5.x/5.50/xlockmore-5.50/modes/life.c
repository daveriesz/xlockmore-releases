		return;
	lp->foundBirth = lp->foundSurvival = False;
	lp->input_param.birth = lp->input_param.survival = 0;
	for (n = 0; n < MAXGROUPS; n++) {
		lp->input_param.birthGroup[n] =
			lp->input_param.survivalGroup[n] = 0;
	}
