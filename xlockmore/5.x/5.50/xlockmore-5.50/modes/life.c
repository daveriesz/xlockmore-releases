static void
flushList(lifestruct * lp, int state)
{
	while (lp->last[state]->previous != lp->first[state]) {
		CellList *curr = lp->last[state]->previous;

		curr->previous->next = lp->last[state];
		lp->last[state]->previous = curr->previous;
		free(curr);
	}
	lp->ncells[state] = 0;
}

